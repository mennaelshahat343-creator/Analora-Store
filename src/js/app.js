document.addEventListener('DOMContentLoaded', () => {
    initTheme();
    initFirebase();
    updateCartCount();
    updateWishCount();
    renderRecommendedProducts();
    initLanguage();

    const kpiWish = document.getElementById('kpi-wishlist');
    if (kpiWish) kpiWish.textContent = wishlist.length;

    showLoading(true);
    setTimeout(() => {
        showLoading(false);
        showPage('welcome-page');
    }, 600);
});

function getTranslation(key) {
    const value = translations[window.currentLanguage]?.[key] ?? translations.ar[key];
    return typeof value === 'string' ? value : key;
}

window.t = getTranslation;

function setLanguage(lang) {
    const language = translations[lang] ? lang : 'ar';
    window.currentLanguage = language;
    localStorage.setItem('siteLanguage', language);
    updateLanguageTexts(language);

    document.querySelectorAll('.lang-btn').forEach((btn) => {
        btn.classList.toggle('active', btn.dataset.lang === language);
    });

    renderRecommendedProducts();
    renderCart();
    renderWishlist();
    renderCheckout();
    if (document.getElementById('products-page')?.classList.contains('active') && typeof currentProductsContext !== 'undefined') {
        showProducts(currentProductsContext.gender, currentProductsContext.category);
    }
    if (document.getElementById('product-detail-page')?.classList.contains('active') && typeof currentProductDetailId !== 'undefined' && currentProductDetailId) {
        showProductDetail(currentProductDetailId);
    }
    if (document.getElementById('analytics-page')?.classList.contains('active')) {
        initAnalyticsPage();
    }
}

function setText(id, value) {
    const el = document.getElementById(id);
    if (el) el.textContent = value;
}

function setAttr(id, attr, value) {
    const el = document.getElementById(id);
    if (el) el.setAttribute(attr, value);
}

